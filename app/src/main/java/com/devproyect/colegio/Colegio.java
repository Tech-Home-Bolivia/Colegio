public class Colegio {
    public int imagen;
    public String nombre;
    public String direccion;
    public String tipo;
    public String turno;
    public String precio;
    public String niveles;
    public String requisitos;
    public String latitud;
    public String longitud;
    public Colegio(int i,String n,String dir,String ti
            ,String tur,String pre,String niv,String req
            ,String lat,String lon){
        imagen=i;  tipo=ti;
        nombre=n;  turno=tur;
        direccion=dir;  precio=pre;
        niveles=niv;    requisitos=req;
        latitud=lat;    longitud=lon;
    }





}
